#3. Find the Simple Interest on Rs. 200 for 5 years at 5% per year
# Given values
Amount = 200  # amount in Rs.
rate_of_interest = 5  # Rate of interest per year in percentage
time = 5  # Time in years

# Calculate simple interest
interest = (Amount * rate_of_interest * time) / 100

# Print the result
print("The simple interest on 200 =",interest)
